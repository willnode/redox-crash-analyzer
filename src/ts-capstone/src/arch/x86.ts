export interface cs_x86_op {
  type: X86; // operand type
  reg?: X86; // register value for REG operand
  imm?: number; // immediate value for IMM operand
  mem?: {
    // base/index/scale/disp value for MEM operand
    segment: X86; // segment register (or X86_REG_INVALID if irrelevant)
    base: X86; // base register (or X86_REG_INVALID if irrelevant)
    index: X86; // index register (or X86_REG_INVALID if irrelevant)
    scale: number; // scale for index register
    disp: number; // displacement value
  };
  size: number; // size of this operand (in bytes).
  // How is this operand accessed? (READ, WRITE or READ|WRITE)
  // This field is combined of cs_ac_type.
  access: number;
  avx_bcast?: X86; // X86 AVX broadcast type, or 0 if irrelevant
  avx_zero_opmask: boolean; // AVX zero opmask {z}
}

