export namespace X86 {
  /**
   * Retrieves the relative address for X86 instructions using RIP-relative addressing mode.
   *
   * NOTE: This assumes CS.OPT_DETAIL and present operands in the insn object.
   * @param insn - The instruction to analyze.
   * @returns The relative address associated with the X86 instruction.
   */
  export function REL_ADDR(insn: any): number {
    return insn.detail.x86.operands[0].type == X86.OP_IMM
      ? insn.detail.x86.operands[0].imm
      : insn.address + insn.size + insn.detail.x86.disp;
  }
}

export class cs_x86 {
  // Instruction prefix, which can be up to 4 bytes.
  // A prefix byte gets value 0 when irrelevant.
  // prefix[0] indicates REP/REPNE/LOCK prefix (See X86.PREFIX_REP/REPNE/LOCK above)
  // prefix[1] indicates segment override (irrelevant for x86_64):
  // See X86.PREFIX_CS/SS/DS/ES/FS/GS above.
  // prefix[2] indicates operand-size override (X86.PREFIX_OPSIZE)
  // prefix[3] indicates address-size override (X86.PREFIX_ADDRSIZE)
  public prefix: number[];
  public opcode: number[]; // Instruction opcode, which can be from 1 to 4 bytes in size. This contains VEX opcode as well. An trailing opcode byte gets value 0 when irrelevant.
  public rex: number; // REX prefix: only a non-zero value is relevant for x86_64
  public addr_size: number; // Address size, which can be overridden with above prefix[5].
  public modrm: number; // ModR/M byte
  public sib: number; // SIB value, or 0 when irrelevant.
  public disp: number; // Displacement value, valid if encoding.disp_offset != 0
  public sib_index: X86; // SIB index register, or X86.REG_INVALID when irrelevant.
  public sib_scale: number; // SIB scale, only applicable if sib_index is valid.
  public sib_base: X86; // SIB base register, or X86.REG_INVALID when irrelevant.
  public xop_cc: X86; // XOP Code Condition
  public sse_cc: X86; // SSE Code Condition
  public avx_cc: X86; // AVX Code Condition
  public avx_sae: boolean; // AVX Suppress all Exception
  public avx_rm: X86; // AVX static rounding mode
  public eflags: number; // EFLAGS updated by this instruction. This can be formed from OR combination of X86.EFLAGS_* symbols above
  public fpu_flags: number; // FPU_FLAGS updated by this instruction. This can be formed from OR combination of X86.FPU_FLAGS_* symbols above
  public op_count: number; // Number of operands of this instruction, or 0 when instruction has no operand.
  public operands: cs_x86_op[]; // operands for this instruction.
  public encoding: {
    // encoding information
    modrm_offset: number; // ModR/M offset, or 0 when irrelevant
    // Displacement offset, or 0 when irrelevant.
    disp_offset: number;
    disp_size: number;
    // Immediate offset, or 0 when irrelevant.
    imm_offset: number;
    imm_size: number;
  };

  constructor(arch_info_ptr: number, Memory: any) {
    this.prefix = [];
    this.opcode = [];
    const encoding_ptr: number = arch_info_ptr + 456;
    for (let i = 0; i < 4; i++) {
      this.prefix[i] = Memory.read(arch_info_ptr + i, 'ubyte');
      this.opcode[i] = Memory.read(arch_info_ptr + i + 4, 'ubyte');
    }
    this.rex = Memory.read(arch_info_ptr + 8, 'ubyte');
    this.addr_size = Memory.read(arch_info_ptr + 9, 'ubyte');
    this.modrm = Memory.read(arch_info_ptr + 10, 'ubyte');
    this.sib = Memory.read(arch_info_ptr + 12, 'ubyte');
    this.disp = Memory.read(arch_info_ptr + 16, 'long');
    this.sib_index = Memory.read(arch_info_ptr + 24, 'i32');
    this.sib_scale = Memory.read(arch_info_ptr + 28, 'byte');
    this.sib_base = Memory.read(arch_info_ptr + 29, 'i32');
    this.xop_cc = Memory.read(arch_info_ptr + 33, 'i32');
    this.sse_cc = Memory.read(arch_info_ptr + 37, 'i32');
    this.avx_cc = Memory.read(arch_info_ptr + 41, 'i32');
    this.avx_sae = Memory.read(arch_info_ptr + 45, 'bool');
    this.avx_rm = Memory.read(arch_info_ptr + 46, 'i32');
    this.eflags = Memory.read(arch_info_ptr + 50, 'ulong');
    this.fpu_flags = Memory.read(arch_info_ptr + 56, 'ulong');
    this.op_count = Memory.read(arch_info_ptr + 64, 'ubyte');
    this.encoding = {
      modrm_offset: Memory.read(encoding_ptr, 'ubyte'),
      disp_offset: Memory.read(encoding_ptr + 1, 'ubyte'),
      disp_size: Memory.read(encoding_ptr + 2, 'ubyte'),
      imm_offset: Memory.read(encoding_ptr + 3, 'ubyte'),
      imm_size: Memory.read(encoding_ptr + 4, 'ubyte'),
    };
    this.operands = [];

    for (let i = 0; i < this.op_count; i++) {
      const op: cs_x86_op = {} as cs_x86_op;
      const op_ptr: number = arch_info_ptr + 72 + i * 48;
      op.size = Memory.read(op_ptr + 32, 'ubyte');
      op.access = Memory.read(op_ptr + 33, 'ubyte');
      op.avx_bcast = Memory.read(op_ptr + 36, 'i32');
      op.avx_zero_opmask = Memory.read(op_ptr + 40, 'bool');
      op.type = Memory.read(op_ptr, 'i32');
      switch (op.type) {
        case X86.OP_REG:
          op.reg = Memory.read(op_ptr + 8, 'i32');
          break;
        case X86.OP_IMM:
          op.imm = Memory.read(op_ptr + 8, 'i64');
          break;
        case X86.OP_MEM:
          op.mem = {
            segment: Memory.read(op_ptr + 8, 'i32'),
            base: Memory.read(op_ptr + 12, 'i32'),
            index: Memory.read(op_ptr + 16, 'i32'),
            scale: Memory.read(op_ptr + 20, 'i32'),
            disp: Memory.read(op_ptr + 24, 'i64'),
          };
          break;
      }
      this.operands[i] = op;
    }
    return this;
  }
}
